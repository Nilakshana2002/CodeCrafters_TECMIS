CREATE TABLE student
(
student_ID VARCHAR(20),
student_Name VARCHAR(50),
NIC INT,
Address VARCHAR(50),
E_Mail VARCHAR(30),
Contact_Num VARCHAR(12),
State VARCHAR(20)
);

CREATE TABLE course(
course_ID VARCHAR(20),
course_Name VARCHAR(20),
credit INT,
course_Type Varchar(3),
Theory_H INT,
Practical_H INT,
Lecture_ID VARCHAR(10)
Department_ID VARCHAR(10),

); 